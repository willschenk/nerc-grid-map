function alt(fields) {
  return {
    rank: 2,
    role: "alternate",
    country: "US",
    ...fields,
  };
}

function slug(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function orgId(org) {
  return org.ncr_id || `SUP-${slug(org.entity_name)}`;
}

function applyAlternates(orgs, label) {
  let updated = 0;
  let missing = [];
  for (const org of orgs) {
    const id = orgId(org);
    const slot = ALTERNATES[id];
    if (!slot) continue;
    if (!org.locations?.length) {
      missing.push(`${id} (no locations[])`);
      continue;
    }
    const idx = org.locations.findIndex((l) => l.rank === 2);
    if (idx < 0) {
      missing.push(`${id} (no rank-2 slot)`);
      continue;
    }
    org.locations[idx] = { ...org.locations[idx], ...slot };
    updated++;
  }
  console.log(`apply-location-alternates: ${label} — ${updated} updated`);
  if (missing.length) console.warn("  skipped:", missing.join("; "));
  return updated;
}

const geoPath = resolve(root, "src/data/nerc/geocoded-orgs.json");
const supPath = resolve(root, "src/data/nerc/supplemental-orgs.json");

const geo = JSON.parse(readFileSync(geoPath, "utf8"));
const sup = JSON.parse(readFileSync(supPath, "utf8"));

const geoCount = applyAlternates(geo.orgs, "geocoded-orgs.json");
const supCount = applyAlternates(sup, "supplemental-orgs.json");

writeFileSync(geoPath, JSON.stringify(geo, null, 2) + "\n");
writeFileSync(supPath, JSON.stringify(sup, null, 2) + "\n");

const expected = Object.keys(ALTERNATES).length;
const total = geoCount + supCount;
console.log(`apply-location-alternates: ${total}/${expected} alternates applied`);
if (total !== expected) process.exitCode = 1;
